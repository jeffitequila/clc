package calculadora;

public class Operacao {
  
    public float soma ( float n1, float n2 ){
        float res;
        res = n1 + n2;
        return res;
     
        
    }
    
    public float subtracao ( float n1, float n2 ){
        float res;
        res = n1 - n2;
        return res;
     
        
    }  
    public float multiplicacao ( float n1, float n2 ){
        float res;
        res = n1 * n2;
        return res;
     
        
    }
    public float divisao ( float n1, float n2 ){
        float res;
        res = n1 / n2;
        return res;
     
        
    }
}
